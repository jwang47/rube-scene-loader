package com.stormpz.rube;

/**
 *
 */
public class RUBEVertices {

  private final float[] x;
  private final float[] y;

  public RUBEVertices(float[] x, float[] y) {
    this.x = x;
    this.y = y;
  }

  public float[] getX() {
    return x;
  }

  public float[] getY() {
    return y;
  }
}
