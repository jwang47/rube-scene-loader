package com.stormpz.rube;

/**
 *
 */
public class RUBEVector {

  private final float x;
  private final float y;

  public RUBEVector(float x, float y) {
    this.x = x;
    this.y = y;
  }

  public float getX() {
    return x;
  }

  public float getY() {
    return y;
  }
}
